

# ==  (შედარების ოპერატორი)
a = 5
b = 5
if a == b:
    print("a და b თანაბარია")
# != (არ არის თანაბარი)
a = 5
b = 10
if a != b:
    print("a და b არ არის თანაბარი")
# > (მეტი)
a = 10
b = 5
if a > b:
    print("a მეტია b-ზე")
# < (მცირე)
a = 5
b = 10
if a < b:
    print("a ნაკლებია b-ზე")
# >= (მეტი ან თანაბარი)
a = 10
b = 10
if a >= b:
    print("a მეტია ან თანაბარია b-ზე")

