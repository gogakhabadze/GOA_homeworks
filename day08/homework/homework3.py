# int() - გარდაქმნის მონაცემს მთელ რიცხვად (integer)
# მაგ: სტრინგი "25" გადაიქცევა რიცხვ 25-ად
ricxvi = int("25")
print(ricxvi)           # გამოიტანს: 25
print(type(ricxvi))     # გამოიტანს: <class 'int'>


# float() - გარდაქმნის მონაცემს ათწილადად (floating point number)
# მაგ: სტრინგი "3.14" გადაიქცევა ათწილად 3.14-ად
atwiladi = float("3.14")
print(atwiladi)         # გამოიტანს: 3.14
print(type(atwiladi))   # გამოიტანს: <class 'float'>

# str() - გარდაქმნის ნებისმიერ მონაცემს ტექსტად (სტრინგად)
# მაგ: რიცხვი 100 გადაიქცევა სტრინგ "100"-ად

texti = str(100)
print(texti)            # გამოიტანს: "100"
print(type(texti))      # გამოიტანს: <class 'str'>
