favorite_number = 9  # საყვარელი რიცხვი
age = 15  # თქვენი ასაკი

# მათემატიკური ოპერაციები
sum_result = favorite_number + age  # დამატება
difference_result = favorite_number - age  # გამოკლება
product_result = favorite_number * age  # გამრავლება
quotient_result = favorite_number / age  # დაყოფა
floor_division_result = favorite_number // age  # მთელი ნაწილი (დაყოფა)
remainder_result = favorite_number % age  # ნარჩენი (დაყოფა)
exponentiation_result = favorite_number ** age  # ამაღლება ძალაში

# შედეგების დაბეჭდვა
print("Sum:", sum_result)
print("Difference:", difference_result)
print("Product:", product_result)
print("Quotient:", quotient_result)
print("Floor Division:", floor_division_result)
print("Remainder:", remainder_result)
print("Exponentiation:", exponentiation_result)
